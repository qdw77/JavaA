package ch02.sec03;

public class CharExample {

	public static void main(String[] args) {
		char c1='A';
		char c2=65;
		
		char c3 = '가';
		char c4 = 44032;
		
		System.out.println(c1);
		System.out.println(c2);
		
		System.out.println(c3);
		System.out.println(c4);
		
		// char 타입 변수에 초기화 목적이라면 공백은 유니코드에 저장되어 있음

	}

}
